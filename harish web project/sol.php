1. 	
A train running at the speed of 60 km/hr crosses a pole in 9 seconds. What is the length of the train? <br>

A.	120 metres<br>
B.	180 metres<br>
C.	324 metres<br>
D.	150 metres<br>
Answer: Option D<br>

Explanation:

Speed=60(5/18)	m/sec=(50/3)	m/sec.<br>
Length of the train = (Speed x Time).<br>

 Length of the train =		(50/3)	x 9	m = 150 m.


</p>
