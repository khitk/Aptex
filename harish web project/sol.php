2. 	
 Two trains running in opposite directions cross a man standing on the platform in 27 seconds and 17 seconds respectively. If they cross each other in 23 seconds, what is the ratio of their speeds? <br>

A.	Insufficient data<br>
B.	3:1<br>
C.	1:3<br>
D.	3:2<br>
Answer: Option D<br>

Explanation:

Let speed of the trains be x m/s and y m/s respectively.<br>
Length of first train =27x <br>
Length of second train =17y<br>

Relative speed =x+y <br>
Time taken to cross each other =23s<br>
(27x+17y)/(x+y)=23<br>
(27x+17y)=23(x+y)<br>
4x=6y<br>
x/y=3/2<br>
</p>
